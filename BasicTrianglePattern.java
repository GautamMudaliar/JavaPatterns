class BasicTrianglePattern {
    public static void main(String[] args) {
        
        int rows = 4;                 
        //int columns = 5; not relevant  
        
        for (int j = 0 ; j < rows ; j++){
        for (int i =0 ; i<= j ; i ++){
            
            System.out.print("*");
        }
        System.out.println("");
        }
        
        
    }

}
